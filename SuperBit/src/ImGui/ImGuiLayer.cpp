	float ImGuiLayer::CursorCenteredHorizontally(int size) {
		float windowWidth = ImGui::GetWindowSize().x;
		return ((windowWidth - size) / 2);
	}

	float ImGuiLayer::CursorCenteredVertically(int size) {
		auto windowWidth = ImGui::GetWindowSize().y;
		return ((windowWidth - size) / 2);
	}

